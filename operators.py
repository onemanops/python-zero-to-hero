# Operators:
 
# Assignment operator
x = 10
y = 5

# Arithmetic operators
print(x + y)  # 15
print(x - y)  # 5
print(x * y)  # 50
print(x / y)  # 2.0

# Comparison operators
print(x == y)  # False
print(x != y)  # True
print(x > y)  # True
print(x < y)  # False

# Logical operators
flag1 = True
flag2 = False
print(flag1 and flag2)  # False
print(flag1 or flag2)  # True
print(not flag1)  # False
